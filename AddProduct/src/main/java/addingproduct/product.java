package addingproduct;

public class product {

private int id;
private String name;
private int price;
public int getid() {
return id;
}
public void setid(int id) {
this.id = id;
}
public String getname() {
return name;
}
public void setName(String name) {
this.name = name;
}
public int getprice() {
return price;
}
public void setPrice(int price) {
this.price = price;
}

}
